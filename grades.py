marks = int(input("Enter your marks : "))

if marks >= 90 :
    print("You Got 'A' Grade !")
elif marks >= 80 :
    print("You Got 'B' Grade !")
elif marks >= 70 :
    print("You Got 'C' Grade !")
elif marks >= 60 :
    print("You Got 'D' Grade !")
elif marks >= 50 :
    print("You Got 'E' Grade !")
else:
    print("You have failed the exam!")
    


